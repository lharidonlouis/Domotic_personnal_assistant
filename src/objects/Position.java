package objects;

public class Position {
	private int posX;
	private int posY;
	
	public Position(int posX, int posY){
		this.posX = posX;
		this.posY = posY;
	}
	
	public String getPosX(){
		return posX;
	}
	
	public String getPosY(){
		return posY;
	}
	
	public void setPosX(int posX){
		this.posX;
	}
	
	public void setPosY(int posY){
		this.posY;
	}
}
